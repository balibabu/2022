'''length of string'''
msg='Hello Gita'
print(len(msg))
'''slicing of string'''
print(msg[1:5])
print(msg[-1:-4])

print(msg[-1:])
print(msg[:5])
print(msg[:15])
print('msg[5:] =>',msg[5:])
print('msg[None:5] =>',msg[None:5])
print('msg[1:None] =>',msg[1:None])
print('msg[-4:] =>',msg[-4:])
print('msg[0:10:2] =>',msg[0:10:2])
print('msg[-1:-5:-1] =>',msg[-1:-5:-1])

